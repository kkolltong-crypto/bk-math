function shuffleWithSeed(arr, seed) {
  const a = [...arr];
  let s = seed;
  for (let i = a.length - 1; i > 0; i--) {
    s = (s * 1664525 + 1013904223) & 0xffffffff;
    const j = Math.abs(s) % (i + 1);
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function getSessionQuestions(unitName, round) {
  const qs = DB[unitName].questions;
  return shuffleWithSeed(qs, round * 997 + unitName.length * 31).slice(0, Q_PER_SESSION);
}

